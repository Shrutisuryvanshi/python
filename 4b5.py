set1 = {1, 2, 3, 4, 5}
set2 = {4, 5, 6, 7, 8}


set_difference = set1.difference(set2)
print("Set Difference (set1.difference(set2)):", set_difference)


symmetric_difference = set1.symmetric_difference(set2)
print("Symmetric Difference (set1.symmetric_difference(set2)):", symmetric_difference)
